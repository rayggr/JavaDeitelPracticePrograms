package cap4;

public class Incremento {
	public static void main(String[] args) {
		int c = 5;
		System.out.printf("c antes del posincremento %d%n", c);
		System.out.printf(" postincremento de c %d%n", c++);
		System.out.printf("c despues del posincremento %d%n", c);

		System.out.printf("c antes del preicremento %d%n", c);
		System.out.printf(" preincremento de c %d%n", ++c);
		System.out.printf("c despues del  preincremento %d%n", ++c);

	}

}
